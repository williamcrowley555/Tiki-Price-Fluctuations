package com.client.util;

import java.util.Map;
import java.util.Map.Entry;

/**
 *
 * @author HP
 */
public class MapUtil {
    
    public static <K, V> K getKey(Map<K, V> map, V value) {
        for (Entry<K, V> entry : map.entrySet()) {
            if (entry.getValue().equals(value)) {
                return entry.getKey();
            }
        }
        return null;
    }
    
}
